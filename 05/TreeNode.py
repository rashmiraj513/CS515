class TreeNode:
    def __init__(self, value):
        """
        Initialize a tree node with a given value.
        """
        self.value = value
        self.left = None
        self.right = None

class AVLTreeNode:
    def __init__(self, value):
        """
        Initialize a tree node with a given value.
        """
        self.value = value
        self.left = None
        self.right = None
        self.height = 1